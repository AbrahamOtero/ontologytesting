private void aceptarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarButtonActionPerformed
// TODO add your handling code here:
    AniadirPanelDeAviso panelAviso = new AniadirPanelDeAviso();//GEN-LAST:event_aceptarButtonActionPerformed
    boolean res = FileChooserSelector.deleteDirectory(FileChooserSelector.getPathDirectorioProyecto(),borrarCarpetaCheckBox.isSelected());
    setProyectoBorrado(res);
    if(res==true){
        Configuration.getInstance().cambiarPath(Configuration.getInstance().getHome());
        panelAviso.confirmAction("Proyecto Borrado", MainApplicationJFrame.getInstance());
    }else{
        panelAviso.errorAction("No se pudo borrar el proyecto", MainApplicationJFrame.getInstance());
    }
    this.setVisible(false);
}

