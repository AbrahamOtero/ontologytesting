private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
// TODO add your handling code here:
    CardLayout cl = (CardLayout)(mainPanel.getLayout());
    String card = "";
    if(currentDisplayNum == 1 ) {
    }else if(currentDisplayNum == 3){
        backButton.setEnabled(true);
        if(instances==false){
          card = String.valueOf(currentDisplayNum - 2);
          currentDisplayNum=1;
          backButton.setEnabled(false);
        }else{
          card = String.valueOf(currentDisplayNum - 1);
          currentDisplayNum = 2;
        }
    }else if(currentDisplayNum == 4){
        backButton.setEnabled(true);
        if(test==false){
          card = String.valueOf(currentDisplayNum - 3);
          currentDisplayNum=1;
          backButton.setEnabled(false);
        }else if(test==true){
          card = String.valueOf(currentDisplayNum - 1);
          currentDisplayNum = 3;
        }
    }else if(currentDisplayNum == 5){
        backButton.setEnabled(true);
        if(test==false && instances==false && query==false){
          card = String.valueOf(currentDisplayNum - 4);
          currentDisplayNum=1;
          backButton.setEnabled(false);
        }else if(test==true && query==false){
          card = String.valueOf(currentDisplayNum - 2);
          currentDisplayNum = 3;
        }else if(query==true){
          card = String.valueOf(currentDisplayNum - 1);
          currentDisplayNum = 4;        
        }
        nextButton.setText("Siguiente");
    }
    cl.show(mainPanel, card);
}//GEN-LAST:event_backButtonActionPerformed

private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
// TODO add your handling code here:
    CardLayout cl = (CardLayout)(mainPanel.getLayout());
    String card="";
    if(currentDisplayNum == 1 ) {
        backButton.setEnabled(true);
        if(SPARQLRadioButton.isSelected() && testsRadioButton.isSelected()){
            card = String.valueOf(currentDisplayNum + 2);
            test=true;
            query=true;
            instances=false; 
            currentDisplayNum+=2;
        }else if(testsRadioButton.isSelected()){
            card = String.valueOf(currentDisplayNum + 2);
            test=true;
            query=false;
            instances=false; 
            currentDisplayNum+=2;
        }else if(SPARQLRadioButton.isSelected()){
            card = String.valueOf(currentDisplayNum + 3);
            test=false;
            query=true;
            instances=false;       
            currentDisplayNum+=3;
        }else{
            JOptionPane.showMessageDialog(null,"Debe seleccionar una acci√≥n", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
    }else if(currentDisplayNum == 3){
        backButton.setEnabled(true);
            if(query==true){
                card = String.valueOf(currentDisplayNum + 1);
                currentDisplayNum++;
            }else if(query==false){
                card = String.valueOf(currentDisplayNum + 2);
                currentDisplayNum+=2;
                nextButton.setText("Finalizar");
            }
    }else if(currentDisplayNum == 4){
        backButton.setEnabled(true);
        card = String.valueOf(currentDisplayNum + 1);
        nextButton.setText("Finalizar");
        currentDisplayNum++; 
    }else if(currentDisplayNum == 5){
        backButton.setEnabled(true);   
        nextButton.setText("Siguiente");
            if(yesCheckBox.isSelected()){
                card = String.valueOf(currentDisplayNum - 4);
                currentDisplayNum=1;  
            }else{
                dispose(); 
            }
       yesCheckBox.setSelected(false);
    }
    cl.show(mainPanel, card);
}//GEN-LAST:event_nextButtonActionPerformed

