public boolean consultaCompletaEnAyuda(){
    if((!this.getQuery().equals("") && this.getQueryResult().equals("")) ||
        this.getQuery().equals("") && !this.getQueryResult().equals("")){
        return false;
    }else{
        return true;
    }
}

public boolean panelVacio(){
    if(this.getQuery().equals("") && this.getQueryResult().equals("")){
        return true;
    }else{
        return false;
    }
}

